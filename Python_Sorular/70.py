#  Python'da hangi tür hataları yakalamak için exception handling kullanabilirsiniz?
"""

1-ArithmeticError: Aritmetik işlemler sırasında ortaya çıkan hataları temsil eder. Örneğin, sıfıra bölme hatası (ZeroDivisionError) bu kategoriye girer.

2-AssertionError: assert ifadesinin yanlış olduğu durumlarda ortaya çıkar.

3-FileNotFoundError: Bir dosyanın bulunamadığı durumda oluşan hata.

4-IndexError: Bir dizinin veya listesinin geçersiz bir dizinle erişilmeye çalışılması durumunda oluşan hata.

5-KeyError: Bir sözlükte bulunmayan bir anahtarın kullanılması durumunda oluşan hata.

6-TypeError: Veri türü uyumsuzluklarından kaynaklanan hataları temsil eder. Örneğin, bir sayıyı bir dizeyle birleştirmeye çalışmak gibi.

7-ValueError: Doğru veri türüne sahip olsa da, geçersiz bir değer kullanıldığında ortaya çıkan hata.

8-ZeroDivisionError: Bir sayının sıfıra bölünmeye çalışılması durumunda oluşan hata.

9-IOError: Giriş/çıkış işlemleri sırasında dosya işleme hatası oluştuğunda ortaya çıkan hata.

10-Exception: Tüm exception türlerinin temel sınıfıdır. Bu, diğer hataları yakalamak için genel bir except bloğu kullanmak istediğimiz durumlarda kullanılabilir."""